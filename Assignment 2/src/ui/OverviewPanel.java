    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) viewTable.getModel();
        model.setRowCount(0);
        
        for(Car c: myCarList.getCarArray()){
            Object[] row = new Object[4];
            row[0] = c.getCompany();
            row[1] = c.getModelName();
            row[2] = c.getSeats();
            row[3] = c.getCity();
            
            model.addRow(row);
        }
    }
