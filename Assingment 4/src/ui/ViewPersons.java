    private void populateTable(PersonsList personsList) {
        DefaultTableModel model = (DefaultTableModel) personsTable.getModel();
        model.setRowCount(0);
        
        for(Person p:personsList.getPersonsList()) {
           Object[] row = new Object[4];
           row[0] = p;
           row[1] = p.getAge();
           row[2] = p.getSex();
           row[3] = p.getHouseAddr();
           
           model.addRow(row);
        }
    }
